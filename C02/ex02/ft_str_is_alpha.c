int	ft_str_is_alpha(char *str)
{
	int		i;

	i = 0;
	if (str[i] == '\0')
		return (1);
	else
	{
		while (str[i])
		{
			if (str[i] <= 'z' && str[i] >= 'a')
				i++;
			else if (str[i] <= 'Z' && str[i] >= 'A')
				i++;
			else
				return (0);
		}
		return (1);
	}
}
