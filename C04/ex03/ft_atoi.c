int	ft_atoi(char *str)
{
	int		i;
	int		c_minus;
	int		n;

	i = 0;
	c_minus = 1;
	n = 0;
	while (str[i] && ((str[i] >= 9 && str[i] <= 13) || str[i] == 32))
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			c_minus *= -1;
		i++;
	}
	while (str[i] && (str[i] >= '0' && str[i] <= '9'))
	{
		n = n * 10 + str[i] - 48 ;
		i++;
	}
	return (n * c_minus);
}
