#include <unistd.h>

int	fn(int i)
{
	char	c;
	char	c2;
	
	if (i <= 5)
	{
		c = i +'0';
		write(1, &c, 1);
		i += 1;
		fn(i);
		write(1, "Fn", 2);
		c2 = i +'0';
		write(1, &c2, 1);
		return (i);
	}
	else
		return (0);
}

int main()
{
	fn(0);
	return (0);
}
