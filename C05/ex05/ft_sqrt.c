int	ft_sqrt(int nb)
{
	int	i;

	i = 2;
	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (1);
	else
	{
		while (i < nb / i)
			i++;
		if (i == nb)
			return (0);
		else if (nb % i == 0)
			return (i);
		else
			return (0);
	}
}
